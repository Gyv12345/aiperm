-- ============================================================
-- V4.12.0 恢复 routes.ts 所需但被软删的菜单
-- ------------------------------------------------------------
-- 背景：前端 frontend-react/config/routes.ts 定义了以下菜单，
-- 但数据库 sys_menu 中对应记录被误软删（deleted=1），导致侧边栏
-- 只剩「系统管理」「企业管理」两个目录，监控中心等缺失。
--
-- 说明：工作台 /welcome 是固定首页（layout 写死，access=canUser），
-- 不依赖任何菜单权限，因此「仪表板 /dashboard(1)」目录与
-- 「首页(2)」菜单无需恢复，保持软删状态即可。
--
-- 处理方式：仅把 routes.ts 需要的菜单记录恢复（deleted=1→0），
-- 不动 agent / permission / message-template / message-log 等
-- routes.ts 不涉及的菜单（保持软删状态）。
--
-- 需恢复清单（与 routes.ts 对齐）：
--   18 IM平台配置              —— /system/im-config
--   19 审批场景管理            —— /system/approval-scene
--   40 监控中心 /monitor       —— 监控6个子项的父目录（43~46 未删）
--   41 在线用户                —— /monitor/online
--   42 服务监控                —— /monitor/server
-- 同时连带恢复其下按钮权限（181/182/191-194 等）。
-- 幂等，可重复执行。
-- ============================================================

UPDATE `sys_menu`
SET `deleted` = 0
WHERE `id` IN (
  18,   -- IM平台配置
  19,   -- 审批场景管理
  40,   -- 监控中心 /monitor
  41,   -- 在线用户
  42,   -- 服务监控
  -- 连带恢复的按钮权限子项（菜单恢复后子项需一并可见）
  181,  -- IM配置查询
  182,  -- IM配置修改
  191,  -- 审批场景查询
  192,  -- 审批场景新增
  193,  -- 审批场景修改
  194   -- 审批场景删除
);
